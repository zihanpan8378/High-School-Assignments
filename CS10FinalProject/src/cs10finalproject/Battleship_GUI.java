    private void btnBattleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBattleActionPerformed
        // TODO add your handling code here:
        if (b.remain[1] == 0 && b.remain[2] == 0 && b.remain[3] == 0 && b.remain[4] == 0) {
            b.remain[1] = 1;
            b.remain[2] = 2;
            b.remain[3] = 3;
            b.remain[4] = 4;
            Bombing.setVisible(true);
            Setting.setVisible(false);
            for (int i = 0; i < labels.length; i++) {
                for (int q = 0; q < labels.length; q++) {
                    Bombing.add(labels[i][q]);
                }
                Bombing.add(numbers[i]);
                Bombing.add(letters[i]);
            }
            numbersB = new JLabel[10];
            for (int i = 0; i < numbers.length; i++) {
                numbersB[i] = new JLabel();
                numbersB[i].setVisible(true);
                numbersB[i].setSize(28, 28);
                numbersB[i].setLocation(28 + 350, (i + 1) * 28);
                numbersB[i].setText(String.valueOf(i + 1));
                Bombing.add(numbersB[i]);

            }
            lettersB = new JLabel[10];
            for (int i = 0; i < lettersB.length; i++) {
                lettersB[i] = new JLabel();
                lettersB[i].setVisible(true);
                lettersB[i].setSize(28, 28);
                lettersB[i].setLocation((i + 2) * 28 + 360, 0);
                lettersB[i].setText(letter[i]);
                Bombing.add(lettersB[i]);
            }
            b.FinishSetting();
            b.setEmpty();
            b.setRandom();
            b.setComputer();
            labelsB = new JLabel[10][10];
            for (int i = 0; i < labelsB.length; i++) {
                for (int q = 0; q < labelsB.length; q++) {
                    labelsB[i][q] = new JLabel();
                    labelsB[i][q].setVisible(true);
                    labelsB[i][q].setSize(28, 28);
                    labelsB[i][q].setLocation((i + 1) * 28 + 428 - 50, (q + 1) * 28 + 5);
                    labelsB[i][q].setIcon(b.ic[0]);
                    Bombing.add(labelsB[i][q]);
                }
            }
        } else {
            lblRange.setText("Please finish your setting.");
        }
    }//GEN-LAST:event_btnBattleActionPerformed

    private void btnEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnterActionPerformed
        // TODO add your handling code here:
        int ln = 0;
        for (int i = 0; i < 10; i++) {
            if (!l[ln].equals(txtEnterX.getText()) && !letter[ln].equals(txtEnterX.getText())) {
                ln++;
            }
        }
        b.setX(ln);
        b.setY((int) Double.parseDouble(txtEnterY.getText()) - 1);
        b.type = cobType.getSelectedIndex();
        b.setShipHead();
        b.direction = cobDirection.getSelectedIndex();
        b.setShipBody();

        for (int i = 0; i < labels.length; i++) {
            for (int q = 0; q < labels.length; q++) {
                labels[i][q].setIcon(b.set[i][q]);
            }
        }
        lblRange.setText(b.outRange);

        cobDirection.setSelectedIndex(0);
        cobType.setSelectedIndex(0);
        txtEnterX.setText("");
        txtEnterY.setText("");
        lblR1.setText(String.valueOf(b.remain[1]));
        lblR2.setText(String.valueOf(b.remain[2]));
        lblR3.setText(String.valueOf(b.remain[3]));
        lblR4.setText(String.valueOf(b.remain[4]));
    }//GEN-LAST:event_btnEnterActionPerformed

