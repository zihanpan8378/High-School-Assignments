    private void btnBombActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBombActionPerformed
        // TODO add your handling code here:
        if (b.roundsRemain != 0) {
            int ln = 0;
            while (!(l[ln].equals(txtBX.getText()) || letter[ln].equals(txtBX.getText()))) {
                ln++;
            }
            b.x = ln;
            b.y = (int) Double.parseDouble(txtBY.getText()) - 1;
            b.setBombing();
            labelsB[b.x][b.y].setIcon(b.set[b.x][b.y]);
            b.setComputerBombing();
            for (int i = 0; i < labels.length; i++) {
                for (int q = 0; q < labels.length; q++) {
                    labels[i][q].setIcon(b.set_[i][q]);
                }
            }
            lblBombingResultA.setText(b.resultA);
            lblBombingResultB.setText(b.resultB);
            if (b.roundsRemain > 0) {
                lblNumRound.setText(String.valueOf(b.roundsRemain));
            } else {
                lblNumRound.setText("No rounds remain");
            }
            lblPointA.setText(String.valueOf(b.pointA));
            lblPointB.setText(String.valueOf(b.pointB));
            txtBX.setText("");
            txtBY.setText("");

            if (b.roundsRemain == 0) {
                btnBomb.setIcon(b.ic[9]);
            }
        } else {
            Result.setVisible(true);
            Bombing.setVisible(false);
            lblFinalScoreA.setText(String.valueOf(b.pointA));
            lblFinalScoreB.setText(String.valueOf(b.pointB));
            b.PrintResult();
            lblWinner.setText(b.finalResult);
        }
    }//GEN-LAST:event_btnBombActionPerformed
